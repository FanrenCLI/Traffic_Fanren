package com.fanren.Traffic.pojo;



/**
 * 出租车实体
 * @author fanren
 *
 */
public class taxiInfo {

	private String id;
	private String lon;
	private String lat;
	
	public String getid() {
		return id;
	}
	public void setid(String id) {
		this.id=id;
	}
	public String getlon() {
		return lon;
	}
	public void setlon(String lon) {
		this.lon=lon;
	}
	public String getlat() {
		return lat;
	}
	public void setlat(String lat) {
		this.lat=lat;
	}
}
